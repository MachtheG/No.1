// Generates a simple, valid multi-page PDF of the manifesto into
// public/downloads/. Run: node scripts/gen-manifesto-pdf.mjs
import { writeFileSync, mkdirSync } from "node:fs";

// --- Manifesto content (kept in sync with data/manifesto.ts) ---
const PILLARS = [
  {
    n: "01",
    title: "Agricultural Transformation & Food Security",
    summary:
      "Rebuilding the backbone of the rural economy - inputs, credit, and markets designed around the smallholder farmer, not around cartels.",
    commitments: [
      "Subsidized, e-verified fertilizer and certified seed for smallholders",
      "Warehouse receipt financing so farmers can bank their harvest",
      "Revival of collapsed cooperatives, factories, and produce boards",
    ],
  },
  {
    n: "02",
    title: "MSME Economy & Financing",
    summary:
      "The 85% of Kenya's workforce in the informal sector are the real economy - this pillar puts collateral-free capital directly in their hands.",
    commitments: [
      "The Hustler Fund: collateral-free micro-loans via mobile phone",
      "A dedicated MSME credit guarantee scheme",
      "Formalization pathways that don't punish informal traders",
    ],
  },
  {
    n: "03",
    title: "Housing & Settlement",
    summary:
      "Dignity over survival - a nationwide construction drive that builds homes and creates jobs on-site, county by county.",
    commitments: [
      "Affordable Housing Programme units across all 47 counties",
      "Public land converted into planned, serviced settlements",
      "Construction-sector jobs prioritized for local youth",
    ],
  },
  {
    n: "04",
    title: "Healthcare Access",
    summary:
      "Universal coverage that follows the patient, not the payslip - replacing a fragmented NHIF with needs-based national insurance.",
    commitments: [
      "The Social Health Authority (SHA): universal registration and coverage",
      "A dedicated fund for specialized and catastrophic-cost surgery",
      "Primary care expansion through community health promoters",
    ],
  },
  {
    n: "05",
    title: "Digital Superhighway & Creative Economy",
    summary:
      "Connectivity and content as economic infrastructure - putting Kenya's youth and creators onto the global digital economy.",
    commitments: [
      "Nationwide fibre rollout and affordable last-mile connectivity",
      "Digital jobs and outsourcing hubs for young graduates",
      "Legal and financial infrastructure for the creative industries",
    ],
  },
];

// --- Build the flowing list of lines (text, font size, style) ---
const LINE = [];
const push = (text, size = 11, bold = false, gap = 0) =>
  LINE.push({ text, size, bold, gap });

push("RUTO 2027", 26, true);
push("The Bottom-Up Economic Transformation Agenda", 15, true, 6);
push("My Manifesto for a Second Term", 12, false, 4);
push("KAZI IENDELEE - Let the Work Continue", 11, true, 18);

function wrap(text, max) {
  const words = text.split(" ");
  const lines = [];
  let cur = "";
  for (const w of words) {
    if ((cur + " " + w).trim().length > max) {
      if (cur) lines.push(cur.trim());
      cur = w;
    } else {
      cur += " " + w;
    }
  }
  if (cur) lines.push(cur.trim());
  return lines;
}

for (const p of PILLARS) {
  push(`${p.n}  ${p.title}`, 14, true, 16);
  for (const l of wrap(p.summary, 90)) push(l, 11, false);
  push("", 6);
  for (const c of p.commitments) {
    const lines = wrap("- " + c, 88);
    lines.forEach((l, i) => push(i === 0 ? l : "   " + l, 11, false));
  }
}

push("", 20);
push(
  'Source: "The Plan" - Kenya Kwanza Bottom-Up Economic Transformation Agenda 2022-2027.',
  9,
  false
);
push("This document is a demonstration export generated by the campaign site.", 9, false);

// --- Paginate ---
const PAGE_H = 792;
const PAGE_W = 612;
const TOP = 740;
const BOTTOM = 60;
const LEFT = 60;

const pages = [];
let cur = [];
let y = TOP;
for (const item of LINE) {
  const lh = item.size + 6 + item.gap;
  if (y - lh < BOTTOM) {
    pages.push(cur);
    cur = [];
    y = TOP;
  }
  y -= item.gap;
  cur.push({ ...item, y });
  y -= item.size + 6;
}
if (cur.length) pages.push(cur);

// --- Escape PDF strings ---
const esc = (s) => s.replace(/\\/g, "\\\\").replace(/\(/g, "\\(").replace(/\)/g, "\\)");

// --- Build content streams per page ---
function contentFor(items) {
  let s = "";
  for (const it of items) {
    const font = it.bold ? "/F2" : "/F1";
    s += `BT ${font} ${it.size} Tf 0 0 0 rg ${LEFT} ${it.y.toFixed(
      1
    )} Td (${esc(it.text)}) Tj ET\n`;
  }
  return s;
}

// --- Assemble objects ---
const objects = [];
objects.push(null); // 1-indexed

const catalogId = 1;
const pagesId = 2;
const fontRegularId = 3;
const fontBoldId = 4;
const firstPageObj = 5;

const pageIds = pages.map((_, i) => firstPageObj + i * 2);
const contentIds = pages.map((_, i) => firstPageObj + i * 2 + 1);

objects[catalogId] = `<< /Type /Catalog /Pages ${pagesId} 0 R >>`;
objects[pagesId] = `<< /Type /Pages /Kids [${pageIds
  .map((id) => `${id} 0 R`)
  .join(" ")}] /Count ${pages.length} >>`;
objects[fontRegularId] = `<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica >>`;
objects[fontBoldId] = `<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica-Bold >>`;

pages.forEach((items, i) => {
  const stream = contentFor(items);
  objects[pageIds[i]] =
    `<< /Type /Page /Parent ${pagesId} 0 R /MediaBox [0 0 ${PAGE_W} ${PAGE_H}] ` +
    `/Resources << /Font << /F1 ${fontRegularId} 0 R /F2 ${fontBoldId} 0 R >> >> ` +
    `/Contents ${contentIds[i]} 0 R >>`;
  objects[contentIds[i]] = `<< /Length ${stream.length} >>\nstream\n${stream}endstream`;
});

// --- Serialize with xref ---
let pdf = "%PDF-1.4\n";
const offsets = [];
for (let i = 1; i < objects.length; i++) {
  offsets[i] = pdf.length;
  pdf += `${i} 0 obj\n${objects[i]}\nendobj\n`;
}
const xrefStart = pdf.length;
pdf += `xref\n0 ${objects.length}\n`;
pdf += `0000000000 65535 f \n`;
for (let i = 1; i < objects.length; i++) {
  pdf += `${String(offsets[i]).padStart(10, "0")} 00000 n \n`;
}
pdf += `trailer\n<< /Size ${objects.length} /Root ${catalogId} 0 R >>\nstartxref\n${xrefStart}\n%%EOF`;

mkdirSync("public/downloads", { recursive: true });
writeFileSync("public/downloads/ruto-2027-manifesto.pdf", pdf, "latin1");
console.log(`Wrote public/downloads/ruto-2027-manifesto.pdf (${pages.length} pages)`);
